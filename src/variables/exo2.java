package variables;

public class exo2 {
    public static void main(String[] args) {
        /*Opérations décimales et entières
        – Initialisez une variable décimale et une autre entière avec une valeur impaire
        – Divisez ces deux valeurs par 2
        – Affichez le résultat et analysez-le*/

        float x = 20.40580f;
        int y = 7;

        System.out.println(x / 2); // Le résultat renvoyé est un float
        System.out.println(y / 2); // Le résultat envoyé malgrés la division par deux d'un chiffre impaire nous donne une valeur entière
    }
}
